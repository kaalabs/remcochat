import type { UiLanguage } from "@/lib/types";

export type I18nKey =
  | keyof typeof enMessages;

export function isUiLanguage(value: unknown): value is UiLanguage {
  return value === "en" || value === "nl";
}

export function parseUiLanguage(value: unknown): UiLanguage | null {
  return isUiLanguage(value) ? value : null;
}

export function normalizeUiLanguage(
  value: unknown,
  fallback: UiLanguage = "en"
): UiLanguage {
  return parseUiLanguage(value) ?? fallback;
}

export function detectUiLanguageFromAcceptLanguage(
  headerValue: string | null | undefined
): UiLanguage {
  const raw = String(headerValue ?? "").trim();
  if (!raw) return "en";
  const first = raw.split(",")[0]?.trim().toLowerCase() ?? "";
  return first.startsWith("nl") ? "nl" : "en";
}

export function localeForUiLanguage(language: UiLanguage): string {
  return language === "nl" ? "nl-NL" : "en-US";
}

